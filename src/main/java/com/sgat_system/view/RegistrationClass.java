    private void btn_save_registerStu1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_save_registerStu1ActionPerformed

        int id = Integer.parseInt(txt_id_class.getText());
        String nameStudent = String.valueOf(JcomboBox_studentName.getSelectedItem());
        String instructor = String.valueOf(JcomboBox_instructorName.getSelectedItem());
        String categoty = String.valueOf(JcomboBox_category.getSelectedItem());
        String dayClass = txt_day_class.getText();
        String timeClass = String.valueOf(JcomboBox_time.getSelectedItem());
                
        ClassroomController classroom = new ClassroomController();
        
        if (classroom.registerClassroom(id, nameStudent, instructor, categoty, dayClass, timeClass))
        {
            JOptionPane.showMessageDialog(null, "Saved information!", "Successfully", 1);
        }
        else 
        {
            JOptionPane.showMessageDialog(null, "Error saving information!", "Error", 0);
        }
        
        this.dispose();
        Dashboard dash = new Dashboard();
        dash.setVisible(true);
    }//GEN-LAST:event_btn_save_registerStu1ActionPerformed

