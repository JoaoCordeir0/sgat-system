    private void JcomboBox_instructorNameAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_JcomboBox_instructorNameAncestorAdded

        Vector vector = new Vector();
        ClassroomController classrom = new ClassroomController();

        vector = classrom.comboBoxListInstructorInfo();

        if (vector.size() != 0)
        {
            for (int c = 0; c < vector.size(); c++)
            {
                JcomboBox_instructorName.addItem(String.valueOf(vector.get(c)));
            }
        }
        else
        {
            JcomboBox_instructorName.addItem("No instructors registered");
        }
    }//GEN-LAST:event_JcomboBox_instructorNameAncestorAdded

